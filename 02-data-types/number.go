package main

import "fmt"

func main() {
	fmt.Println(" Integer ===>")
	fmt.Println(" 1 + 1 = ", 1+1)
	fmt.Println(" 3 * 3 = ", 3*3)
	fmt.Println(" 10 - 3 = ", 10-3)
	fmt.Println(" 11 % 4 = ", 11%4)

	fmt.Println("Flating point number ===>")
	fmt.Println(" 1.3 * 3 = ", 1.3*3)
	fmt.Println(" 3 / 2 = ", 3/2)
	fmt.Println(" 1.01 - 0.99 = ", 1.01-0.99)
}
